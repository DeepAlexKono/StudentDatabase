package Utility;

public class ConsoleWriter {
    public static void writeln(double a) {
        System.out.println(a);
    }
    public static void writeln(String a) {
        System.out.println(a);
    }
    public static void writeln(boolean a) { System.out.println(a); }

    public static void write(double a) {
        System.out.print(a);
    }
    public static void write(String a) {
        System.out.print(a);
    }
    public static void write(boolean a) { System.out.print(a); }
}
